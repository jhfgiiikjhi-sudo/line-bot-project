function saveUsers() {
  fs.writeFileSync(USERS_FILE, JSON.stringify(users, null, 2));
}

/* ===============================
   HELPER
================================ */
function reply(event, text) {
  if (!event.replyToken) return;
  return client.replyMessage(event.replyToken, {
    type: 'text',
    text
  });
}

function resetUser(userId) {
  users[userId] = {
    step: 'ask_name',
    lastActive: Date.now()
  };
  saveUsers();
}

